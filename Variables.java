public class Variables{
	public static void main(String[] args){
		int diez = 10;
		float doceymedio = 12.5f;
		String cadena = "Java programming";
		
		System.out.print(diez + "\n"+
		                 doceymedio + "\n"+
		                 cadena);
	} 
}